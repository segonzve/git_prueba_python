# operadores de comparacion 
# == es igual que
# != es distinto de 
# < es menor que 
# <= es menor o igual que
# > es mayor que
# >= es mayor o igual que 

igual_que = 5 == 6 # false
distinto_de = 5 != 6 #true
menor_que = 5 < 6 #false
menor_o_igual = 5 <= 6 #true
mayor_que = 5 > 6 #true
mayor_o_igual = 5 >= 6 #false

suma = 5 + 6 <= 7 + 5
print(suma)